package practice.operators;

public class Assignment {
    public static void main(String[] args) {
        int a;
        // for assign a value;
        a=10;
        System.out.println(a);
        //compound assignment operator
        a+=10;// (a=a+10)
        System.out.println(a);

        a-=10;//(a=a-10)
        System.out.println(a);
    }
}
